// template literals
// backticks (`) are used to create a string literal that can contain embedded expressions.

const myName = "Hossain";
const mySurName = "Mazumdar";
const prefix = "Mr.";

console.log(`Hi! ${prefix} ${myName} ${mySurName}, How are you?`);
// Output: Hi! Mr. Hossain Mazumdar, How are you?
